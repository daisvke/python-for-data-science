def count_in_list(input_list, search_value):
    """
    This function counts the occurrences of `search_value` in `input_list`.

    Parameters
    ----------
    input_list: List of items to search through
    aram search_value: The value whose occurrences will be counted

    Returns
    -------
    The number of occurrences of search_value in input_list
    """
    return input_list.count(search_value)
